LR_CONFIG = {
    "SGD": 0.01,
    "Adam": 0.001,
    "RMSprop": 0.001,
}

SEEDS = [0, 1, 2, 3, 4]

# 4. Data
transform = transforms.ToTensor()

train_dataset = torchvision.datasets.MNIST(
    root="./data", train=True, download=True, transform=transform
)

test_dataset = torchvision.datasets.MNIST(
    root="./data", train=False, download=True, transform=transform
)

train_loader = DataLoader(train_dataset, batch_size=BATCH_SIZE, shuffle=True)
test_loader = DataLoader(test_dataset, batch_size=BATCH_SIZE, shuffle=False)

# 5. Model
class MLP(nn.Module):
    def __init__(self):
        super().__init__()
        self.net = nn.Sequential(
            nn.Flatten(),
            nn.Linear(28 * 28, 256),
            nn.ReLU(),
            nn.Linear(256, 128),
            nn.ReLU(),
            nn.Linear(128, 10)
        )

    def forward(self, x):
        return self.net(x)

# 6. Helpers
def evaluate_accuracy(model):
    model.eval()
    correct = 0
    total = 0

    with torch.no_grad():
        for images, labels in test_loader:
            images, labels = images.to(device), labels.to(device)
            outputs = model(images)
            preds = outputs.argmax(dim=1)
            correct += (preds == labels).sum().item()
            total += labels.size(0)

    return 100.0 * correct / total


def compute_weight_norm(model):
    total_sq_norm = 0.0
    with torch.no_grad():
        for param in model.parameters():
            total_sq_norm += torch.sum(param ** 2).item()
    return math.sqrt(total_sq_norm)

# 7. Training
def train_model(optimizer_name):
    model = MLP().to(device)
    criterion = nn.CrossEntropyLoss()

    if optimizer_name == "SGD":
        optimizer = optim.SGD(model.parameters(), lr=LR_CONFIG["SGD"])
    elif optimizer_name == "Adam":
        optimizer = optim.Adam(model.parameters(), lr=LR_CONFIG["Adam"])
    elif optimizer_name == "RMSprop":
        optimizer = optim.RMSprop(model.parameters(), lr=LR_CONFIG["RMSprop"])

    losses = []

    for epoch in range(EPOCHS):
        model.train()
        running_loss = 0.0
        total = 0

        for images, labels in train_loader:
            images, labels = images.to(device), labels.to(device)

            optimizer.zero_grad()
            outputs = model(images)
            loss = criterion(outputs, labels)
            loss.backward()
            optimizer.step()

            running_loss += loss.item() * labels.size(0)
            total += labels.size(0)

        avg_loss = running_loss / total
        losses.append(avg_loss)

    acc = evaluate_accuracy(model)
    norm = compute_weight_norm(model)

    return losses, acc, norm

# 8. Run Experiments
optimizers = ["SGD", "Adam", "RMSprop"]

